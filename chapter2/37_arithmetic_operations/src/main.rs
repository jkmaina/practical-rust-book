// This program demonstrates basic arithmetic operations in Rust.
// It includes addition, subtraction, multiplication, division, and integer division.
// Each operation is printed to the console.

fn main() {
    // Addition
    let sum = 5 + 10;
    println!("5 + 10 = {}", sum);
    
    // Subtraction
    let difference = 95.5 - 4.3;
    println!("95.5 - 4.3 = {}", difference);
    
    // Multiplication
    let product = 4 * 30;
    println!("4 * 30 = {}", product);
    
    // Division
    let quotient = 56.7 / 32.2;
    println!("56.7 / 32.2 = {}", quotient);
    
    // Integer division
    let integer_division = 5 / 2;
    println!("5 / 2 = {}", integer_division); // Outputs: 5 / 2 = 2
}
