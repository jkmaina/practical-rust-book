// This program demonstrates basic type annotations in Rust.
// It defines variables with different types and prints their values.

fn main() {
    let age: u32 = 42;
    let pi: f64 = 3.14159;
    let is_rust_fun: bool = true;
    let letter_a: char = 'A';
    
    println!("age: {}, pi: {}, is_rust_fun: {}, letter_a: {}", 
             age, pi, is_rust_fun, letter_a);
}
