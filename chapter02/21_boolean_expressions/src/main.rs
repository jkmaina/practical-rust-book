// This program checks if it is morning and prints a greeting accordingly.
// It uses a boolean expression to determine the time of day.
// The `is_morning` variable is set to true, and the program prints "Good morning!" if it is true.
// Otherwise, it prints "Good day!".
fn main() {
    let is_morning = true;
    
    if is_morning {
        println!("Good morning!");
    } else {
        println!("Good day!");
    }
}
