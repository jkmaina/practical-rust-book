// This is a simple Rust program that demonstrates the use of if-else statements.
// It checks if a number is greater than 5 and prints a message accordingly.
// The program uses a single if-else structure to determine the output.
fn main() {
    let number = 3;
    
    if number > 5 {
        println!("The number is greater than 5");
    } else {
        println!("The number is 5 or less");
    }
}
